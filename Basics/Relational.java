public class Relational {
    public static void main(String[] args) {
        int B = 5;
        int A = 5;
        System.out.println(A == B);

        int b = 10;
        int a = 5;
        System.out.println(a != b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(a < b);
        System.out.println(a > b); 
    }
}
