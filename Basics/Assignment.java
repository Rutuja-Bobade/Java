public class Assignment {
    public static void main(String args[]){
        int a = 10;
        a += 10;
        System.out.println(a);
        a -= 3;
        System.out.println(a);
        a *= 2;
        System.out.println(a);
        a /= 4;
        System.out.println(a);
    }
}

// at each step the value of a is updated and the updated value is used for the next operation