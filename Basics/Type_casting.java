import java.util.*;

public class Type_casting {
    public static void main(String[] args) {
        Scanner sc =new Scanner(System.in);
        Float abcd =23.43535f;
        int b= (int) 23.43535f;
        System.out.println(abcd);
        System.out.println(b);
        char ch = 'd' ;
        int num = (int)ch;
        System.out.println(num);
        sc.close();
    }
}
